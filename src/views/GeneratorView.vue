<script>
import { ref, provide, readonly } from 'vue'
import { useColorPalette } from '../composables/useColorPalette'
import PaletteGenerator from '../components/PaletteGenerator.vue'
import MoodSelector from '../components/MoodSelector.vue'

export default {
  name: 'GeneratorView',
  
  components: {
    PaletteGenerator,
    MoodSelector
  },
  
  setup() {
    const { colors, updatePalette } = useColorPalette()
    const paletteKey = ref(0) // –ö–ª—é—á –¥–ª—è —Ñ–æ—Ä—Å–∏—Ä–æ–≤–∞–Ω–∏—è –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è PaletteGenerator
    
    const handleMoodPalette = (newColors) => {
      // –û–±–Ω–æ–≤–ª—è–µ–º –ø–∞–ª–∏—Ç—Ä—É —á–µ—Ä–µ–∑ composable
      updatePalette(newColors)
      
      // –§–æ—Ä—Å–∏—Ä—É–µ–º –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–∞ PaletteGenerator
      paletteKey.value++
      
      try {
        localStorage.setItem('currentPalette', JSON.stringify(newColors))
        localStorage.setItem('paletteType', 'mood')
      } catch (error) {
        console.error('Error saving mood palette:', error)
      }
    }
    
    // –ì–µ–Ω–µ—Ä–∞—Ü–∏—è –ø–∞–ª–∏—Ç—Ä—ã –ø–æ —Ç–∏–ø—É –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∏—è
    const generateMoodPalette = (moodType) => {
      let palette = []
      
      switch(moodType) {
        case 'calm':
          // –°–ø–æ–∫–æ–π–Ω—ã–µ —Ç–æ–Ω–∞ - –ø–∞—Å—Ç–µ–ª—å–Ω—ã–µ, –º—è–≥–∫–∏–µ —Ü–≤–µ—Ç–∞
          palette = generateCalmPalette()
          break
        case 'energetic':
          // –≠–Ω–µ—Ä–≥–∏—á–Ω—ã–µ - —è—Ä–∫–∏–µ, –Ω–∞—Å—ã—â–µ–Ω–Ω—ã–µ —Ü–≤–µ—Ç–∞
          palette = generateEnergeticPalette()
          break
        case 'professional':
          // –ü—Ä–æ—Ñ–µ—Å—Å–∏–æ–Ω–∞–ª—å–Ω—ã–µ - —Å–¥–µ—Ä–∂–∞–Ω–Ω—ã–µ, –∫–æ–Ω—Ç—Ä–∞—Å—Ç–Ω—ã–µ
          palette = generateProfessionalPalette()
          break
        case 'creative':
          // –ö—Ä–µ–∞—Ç–∏–≤–Ω—ã–µ - –Ω–µ–æ–±—ã—á–Ω—ã–µ, —ç–∫—Å–ø–µ—Ä–∏–º–µ–Ω—Ç–∞–ª—å–Ω—ã–µ
          palette = generateCreativePalette()
          break
        default:
          palette = generateRandomPalette()
      }
      
      handleMoodPalette(palette)
    }
    
    // –ì–µ–Ω–µ—Ä–∞—Ü–∏—è —Å–ª—É—á–∞–π–Ω–æ–π –ø–∞–ª–∏—Ç—Ä—ã –Ω–∞ –æ—Å–Ω–æ–≤–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∏—è
    const generateRandomMoodPalette = () => {
      const moods = ['calm', 'energetic', 'professional', 'creative']
      const randomMood = moods[Math.floor(Math.random() * moods.length)]
      generateMoodPalette(randomMood)
    }
    
    // –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ —Å–ª—É—á–∞–π–Ω–æ–π –ø–∞–ª–∏—Ç—Ä—ã
    const generateRandomPalette = () => {
      return Array.from({ length: 5 }, () => {
        const hue = Math.floor(Math.random() * 360)
        const saturation = 30 + Math.random() * 70
        const lightness = 30 + Math.random() * 40
        return `hsl(${hue}, ${saturation}%, ${lightness}%)`
      })
    }
    
    // –ì–µ–Ω–µ—Ä–∞—Ç–æ—Ä—ã –ø–∞–ª–∏—Ç—Ä –¥–ª—è —Ä–∞–∑–Ω—ã—Ö –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∏–π
    const generateCalmPalette = () => {
      const hues = [180, 210, 240, 270] // –•–æ–ª–æ–¥–Ω—ã–µ —Ç–æ–Ω–∞
      return Array.from({ length: 5 }, () => {
        const hue = hues[Math.floor(Math.random() * hues.length)]
        const saturation = 30 + Math.random() * 40 // –ù–∏–∑–∫–∞—è-—Å—Ä–µ–¥–Ω—è—è –Ω–∞—Å—ã—â–µ–Ω–Ω–æ—Å—Ç—å
        const lightness = 60 + Math.random() * 25 // –°–≤–µ—Ç–ª—ã–µ —Ç–æ–Ω–∞
        return `hsl(${hue}, ${saturation}%, ${lightness}%)`
      })
    }
    
    const generateEnergeticPalette = () => {
      const hues = [0, 30, 60, 90] // –¢–µ–ø–ª—ã–µ, —è—Ä–∫–∏–µ —Ç–æ–Ω–∞
      return Array.from({ length: 5 }, () => {
        const hue = hues[Math.floor(Math.random() * hues.length)]
        const saturation = 70 + Math.random() * 25 // –í—ã—Å–æ–∫–∞—è –Ω–∞—Å—ã—â–µ–Ω–Ω–æ—Å—Ç—å
        const lightness = 40 + Math.random() * 30 // –°—Ä–µ–¥–Ω—è—è —è—Ä–∫–æ—Å—Ç—å
        return `hsl(${hue}, ${saturation}%, ${lightness}%)`
      })
    }
    
    const generateProfessionalPalette = () => {
      const baseHue = Math.random() * 60 // –°–∏–Ω–∏–µ, —Å–µ—Ä—ã–µ, —Ç–µ–º–Ω—ã–µ —Ç–æ–Ω–∞
      return Array.from({ length: 5 }, (_, i) => {
        const hue = baseHue + (i * 15)
        const saturation = 20 + Math.random() * 40
        const lightness = i === 0 ? 20 : i === 4 ? 80 : 30 + i * 10
        return `hsl(${hue % 360}, ${saturation}%, ${lightness}%)`
      })
    }
    
    const generateCreativePalette = () => {
      // –ö—Ä–µ–∞—Ç–∏–≤–Ω—ã–µ - –Ω–µ–æ–±—ã—á–Ω—ã–µ —Å–æ—á–µ—Ç–∞–Ω–∏—è
      const unusualHues = [280, 320, 140, 200, 30] // –§–∏–æ–ª–µ—Ç–æ–≤—ã–µ, —Ä–æ–∑–æ–≤—ã–µ, —Å–∞–ª–∞—Ç–æ–≤—ã–µ
      return Array.from({ length: 5 }, () => {
        const hue = unusualHues[Math.floor(Math.random() * unusualHues.length)]
        const saturation = 50 + Math.random() * 40
        const lightness = 40 + Math.random() * 40
        return `hsl(${hue}, ${saturation}%, ${lightness}%)`
      })
    }
    
    // –ü—Ä–µ–¥–æ—Å—Ç–∞–≤–ª—è–µ–º —Ñ—É–Ω–∫—Ü–∏—é –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –ø–∞–ª–∏—Ç—Ä—ã –¥–ª—è –¥–æ—á–µ—Ä–Ω–∏—Ö –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–æ–≤
    provide('updatePalette', handleMoodPalette)
    
    return {
      handleMoodPalette,
      generateMoodPalette,
      generateRandomMoodPalette,
      paletteKey
    }
  }
}
